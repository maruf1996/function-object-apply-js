const marks='R';

switch(marks){
    case 'A+':
        console.log('Great');
    break;
    case 'A':
        console.log('Good');
    break;
    case 'A-':
        console.log('Pretty Good');
    break;
    case 'B':
        console.log('Not Bad');
    break;
    case 'C':
        console.log('Something');
    break;
    case 'F':
        console.log('Failed');
    break;
    default:
        console.log('Unknown Grade')
}